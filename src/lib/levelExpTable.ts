/**
 * 레벨업이 포함된 구간의 실제 경험치 획득량 계산
 * - 테이블에 있는 레벨: 정확히 계산
 * - 테이블에 없는 레벨: null 반환 (expLevelChange 추정으로 폴백)
 */
export function calcExpGainWithLevelUp(
  prevLevel: number,
  prevExp: number,
  currLevel: number,
  currExp: number,
): number | null {
  if (currLevel === prevLevel) {
    return currExp - prevExp;
  }

  // 필요한 모든 레벨이 테이블에 있는지 확인
  for (let lv = prevLevel; lv < currLevel; lv++) {
    if (LEVEL_EXP_TABLE[lv] === undefined) return null;
  }

  // 시작 레벨에서 남은 경험치
  const remaining = LEVEL_EXP_TABLE[prevLevel] - prevExp;

  // 중간 레벨 전체 경험치 합산
  let middleSum = 0;
  for (let lv = prevLevel + 1; lv < currLevel; lv++) {
    middleSum += LEVEL_EXP_TABLE[lv];
  }

  // 현재 레벨에서 쌓인 경험치
  return remaining + middleSum + currExp;
}
